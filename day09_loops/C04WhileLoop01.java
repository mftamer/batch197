package day09_loops;

public class C04WhileLoop01 {

    public static void main(String[] args) {

        /*
        for (initial index; condition(ending index); increment/decrement){

            code to be executed

        }

        While Loop:

        initial index;

        while(Condition) {

        code to be executed;
        increment/decrement;

        }
*/

        // Type number from 4 to 14 on console

        //for loop;

        for (int i=4; i<15; i++){
            System.out.println(i + " ");
        }

        //while loop;

        int k=4;
        while (k<15){
            System.out.println(k + " ");
            k++;
        }










    }

}
